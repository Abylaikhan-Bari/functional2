'Екі бүтін А және В саны берілген (А ≤ B бар).'
'А-дан В-ға дейінгі барлық сандарды басып шығарыңыз.'
'Даны два целых числа A и B (при этом A ≤ B). '
'Выведите все числа от A до B включительно.'


a = int(input("a: "))
b = int(input("b: "))
if a > b:
    print("A > B")

else:
    for i in range(a, b+1):
        print(i, end= " ")
