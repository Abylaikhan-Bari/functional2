# Составьте словарь «График отпусков» для специалиста отдела кадров. По известному графику отпусков научитесь определять, у кого отпуск в заданном месяце.
#
# В	первой строчке записано целое число – количество сотрудников. В следующих N строчках записана информация о дате их отпуска. Каждая строчка состоит из трёх частей, разделённых пробелом – фамилии сотрудника, дня и месяца его отпуска.
#
# В	следующей строке записан запрос — это название месяца. Выведите через пробел фамилии всех сотрудников, у которых отпуск в указанном месяце. Если в заданном месяце никто не идет в отпуск, оставьте строку ответа пустой.
# Считываем количество сотрудников
n = int(input())

# Создаем словарь для графика отпусков
vacation_schedule = {}

# Заполняем словарь
for i in range(n):
    name, day, month = input().split()
    if month not in vacation_schedule:
        vacation_schedule[month] = []
    vacation_schedule[month].append(name)

# Считываем месяц, для которого нужно найти отпускающихся
month_query = input()

# Ищем сотрудников, у которых отпуск в заданном месяце
if month_query in vacation_schedule:
    print(' '.join(vacation_schedule[month_query]))
else:
    print()
